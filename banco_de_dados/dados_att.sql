create table moeda (
	idMoeda int primary key auto_increment,
    nome varchar(45)
);
 create table ano (
	idAno int primary key
);
create table moedaAno (
	fkMoeda int,
    fkAno int,
    mes int,
    media decimal(10,2),
    primary key(fkMoeda, fkAno, mes),
    foreign key(fkMoeda) references moeda(idMoeda),
    foreign key(fkAno) references ano(idAno)
);

CREATE TABLE usuario (
	id INT PRIMARY KEY AUTO_INCREMENT,
	nome VARCHAR(50),
	email VARCHAR(50),
	senha VARCHAR(50),
    nivel CHAR(1),
	CONSTRAINT CHK_nivel CHECK (nivel in(1,2,3))
);

CREATE TABLE calls (
	idCall INT PRIMARY KEY AUTO_INCREMENT,
    dataC datetime,
    moeda varchar(20),
    tipo varchar(10),
    compra decimal(10,2),
    saida decimal(10,2),
    venda decimal(10,2)
);

